public enum Digitos
{
    Zero = 0,
    One = 1,
    Two = 1 << 1,    // 2
    Three = 1 << 2, // 4
    Four = 1 << 3, // 8
    Five = 1 << 4, // 16
    Six = 1 << 5, // 32
    Seven = 1 << 6,//  64
    Eight = 1 << 7,// 128
    Nine = 1 << 8,// 256
    Ten = 1 << 9,// 512
    Eleven = 1 << 10,// 1024
}
